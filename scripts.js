let value 

// operador de atribuição =     
value = 1;
console.log(value);

// operador de incremento +=
value += 2;
console.log(value);

// operador de decremento -=
value -= 1;
console.log(value);

// operador de multiplicação *=
value *= 2;
console.log(value);

// operador de divisão /=
value /= 2;
console.log(value);

// operador de exponenciação **=
value **= 2;
console.log(value);

// operador de resto da divisão %=
value %= 2;
console.log(value);


